# Ultima Underworld Debug and Analysis Tools
#
# These are development/debug utilities for analyzing game file formats.
# They are not part of the main extraction pipeline.
#
# Scripts:
#   analyze_lev_ark.py     - LEV.ARK structure analyzer
#   analyze_writings.py    - Analyze writings and gravestones
#   check_categories.py    - Quick JSON category checker
#   check_item.py          - Check specific item raw data
#   check_tmobj_mapping.py - Check TMOBJ image path mapping
#   debug_item_flags.py    - Debug item flags attribute access
#   find_bones.py          - Find bone objects in game data
#   inspect_level_data.py  - Level data byte-level inspector
